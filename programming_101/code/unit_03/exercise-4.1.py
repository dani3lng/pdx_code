# ask for number from user
user_number = int(input('Please enter an integer: '))

# determine if number is between 10 - 100
if user_number >= 10 and user_number <= 100:
    print(f'{user_number} is between 10 and 100')
else:
    print(f'{user_number} is not between 10 and 100')