# find hypotenuse of a triangle
# c^2 = a^2 + b^2
# c = hypotenuse
# a = height
# b = base

import math

# assign number to variables
a = 65
b = 72

# find volume
c = math.sqrt(a**2 + b**2)

# print output
print(f"A triangle with sides of {a} and {b} has a hypotenuse of {c}")